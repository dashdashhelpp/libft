#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	while (lst->next != NULL)
	{
		temp = lst->next;
		(*f)(lst->content);
		lst = temp;
	}
	(*f)(lst->content);
}
